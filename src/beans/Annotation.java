package beans;

public class Annotation {
	private int id;
	private String label;
	private String xpath;
	public int getId() {
		return id;
	}
	public String getLabel() {
		return label;
	}
	public String getXpath() {
		return xpath;
	}
	public void setId(int id) {
		this.id = id;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public void setXpath(String xpath) {
		this.xpath = xpath;
	}
}
